import React, { useState } from "react";
import TodoForm from "./TodoForm";

const Todo = ({ todo, deleteTodo, editTodo, user }) => {
  const [isToEdit, setIsToEdit] = useState(false);

  const getCurrentLoggedInUsername = (userObject) => {
    let currentUsername = null;
    if (userObject) {
      if (userObject.username) {
        currentUsername = userObject.username;
      }
    }
    return currentUsername;
  };

  return (
    <>
      {isToEdit ? (
        <>
          <TodoForm
            content={todo.content}
            isPrivate={todo.isPrivate}
            todoId={todo.id}
            editTodo={editTodo}
            isToEdit={isToEdit}
            setIsToEdit={setIsToEdit}
          />
        </>
      ) : (
        <div className="todo">
          <div>
            <p className="todo-content">
              {todo.content}{" "}
              {todo.isPrivate && (
                <svg
                  xmlns="http://www.w3.org/2000/svg"
                  width="16"
                  height="16"
                  fill="currentColor"
                  class="bi bi-lock"
                  viewBox="0 0 16 16"
                >
                  <path d="M8 1a2 2 0 0 1 2 2v4H6V3a2 2 0 0 1 2-2zm3 6V3a3 3 0 0 0-6 0v4a2 2 0 0 0-2 2v5a2 2 0 0 0 2 2h6a2 2 0 0 0 2-2V9a2 2 0 0 0-2-2zM5 8h6a1 1 0 0 1 1 1v5a1 1 0 0 1-1 1H5a1 1 0 0 1-1-1V9a1 1 0 0 1 1-1z" />
                </svg>
              )}
            </p>
            <p className="todo-owner">Owner: {todo.userId}</p>
          </div>
          <div>
            {todo.userId === getCurrentLoggedInUsername(user) && (
              <>
                <span
                  className="edit-todo-icon"
                  onClick={() => setIsToEdit(true)}
                >
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    width="16"
                    height="16"
                    fill="currentColor"
                    class="bi bi-pencil"
                    viewBox="0 0 16 16"
                  >
                    <path d="M12.146.146a.5.5 0 0 1 .708 0l3 3a.5.5 0 0 1 0 .708l-10 10a.5.5 0 0 1-.168.11l-5 2a.5.5 0 0 1-.65-.65l2-5a.5.5 0 0 1 .11-.168l10-10zM11.207 2.5L13.5 4.793 14.793 3.5 12.5 1.207 11.207 2.5zm1.586 3L10.5 3.207 4 9.707V10h.5a.5.5 0 0 1 .5.5v.5h.5a.5.5 0 0 1 .5.5v.5h.293l6.5-6.5zm-9.761 5.175l-.106.106-1.528 3.821 3.821-1.528.106-.106A.5.5 0 0 1 5 12.5V12h-.5a.5.5 0 0 1-.5-.5V11h-.5a.5.5 0 0 1-.468-.325z" />
                  </svg>
                </span>

                <span
                  className="edit-todo-icon"
                  onClick={() => deleteTodo(todo.id)}
                >
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    width="16"
                    height="16"
                    fill="currentColor"
                    class="bi bi-x"
                    viewBox="0 0 16 16"
                  >
                    <path d="M4.646 4.646a.5.5 0 0 1 .708 0L8 7.293l2.646-2.647a.5.5 0 0 1 .708.708L8.707 8l2.647 2.646a.5.5 0 0 1-.708.708L8 8.707l-2.646 2.647a.5.5 0 0 1-.708-.708L7.293 8 4.646 5.354a.5.5 0 0 1 0-.708z" />
                  </svg>
                </span>
              </>
            )}
          </div>
        </div>
      )}
    </>
  );
};

export default Todo;
